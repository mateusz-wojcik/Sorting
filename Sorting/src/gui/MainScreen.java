    void fillArray(){
        Random r = new Random();
        for(int i = 0; i < array.length; i++){
            array[i] = r.nextInt() ;
        }
    }
    
    void initElements(){
        array = new int[Integer.parseInt(AmountTextField.getText())];
        fillArray();
        
        bubbleSort = new BubbleSort(array.clone(), BubbleProgressBar, BubbleSortTimeLabel); 
        selectSort = new SelectSort(array.clone(), SelectProgressBar, SelectSortTimeLabel);
        insertSort = new InsertSort(array.clone(), InsertProgressBar, InsertSortTimeLabel);
    }
    
