    private void StartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartButtonActionPerformed
        if(!areStopped) {
            initElements();
            bubbleSort.start();
            selectSort.start();
            insertSort.start();    
        }
        else {
            bubbleSort.setStopped(false);
            selectSort.setStopped(false);
            insertSort.setStopped(false);
            areStopped = false;
        }
    }//GEN-LAST:event_StartButtonActionPerformed

    private void StopButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StopButtonActionPerformed
        bubbleSort.setStopped(true);
        selectSort.setStopped(true);
        insertSort.setStopped(true);
        areStopped = true;
    }//GEN-LAST:event_StopButtonActionPerformed

    private void ResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetActionPerformed
        bubbleSort.terminate();
        selectSort.terminate();
        insertSort.terminate();
        areStopped = false;
    }//GEN-LAST:event_ResetActionPerformed

